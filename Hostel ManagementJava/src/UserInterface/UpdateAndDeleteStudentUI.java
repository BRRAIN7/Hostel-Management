    private void populateRoomDropdown() {
        RoomOperations roomOps = new RoomOperations();
        List<String> availableRooms = roomOps.fetchAvailableRooms();

        // Clear the current items in the dropdown
        choice1.removeAll();

        // Add available rooms to the dropdown
        for (String roomNo : availableRooms) {
            choice1.add(roomNo);
        }
    }
    private void searchStudentById(String studentId) {
        StudentController controller = new StudentController();
        Student student = controller.getStudentDetails(studentId);

        if (student != null) {
            currentStudent = student; // initialize the entity object

            // Fill UI with student details
            jTextField2.setText(student.getName());
            jTextField3.setText(student.getGender());
            jTextField4.setText(student.getContactNumber());
            jTextField5.setText(student.getAddress());
            jTextField6.setText("");  // password kept empty for privacy
            choice1.select(student.getroomno()); // assuming room ID is shown in choice
        } else {
            JOptionPane.showMessageDialog(this, "Student not found.");
        }
    }



