    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
        if (currentStudent == null) {
            JOptionPane.showMessageDialog(this, "Search and select a student to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            boolean deleted = new StudentController().deleteStudent(currentStudent.getId());
            if (deleted) {
                JOptionPane.showMessageDialog(this, "Student deleted.");
                resetForm(); // Optional: clears form, implement if not yet
            } else {
                JOptionPane.showMessageDialog(this, "Delete failed.");
            }
        }
    }


    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String query = jTextField1.getText();
        StudentOperations so = new StudentOperations();

        try {
            List<Student> students = so.searchStudent(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // clear existing rows

            for (Student s : students) {
                model.addRow(new Object[]{
                        s.getId(),
                        s.getName(),
                        s.getGender(),
                        s.getContactNumber(),
                        s.getAddress(),
                        s.getroomno()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error while searching: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed
    private void resetForm() {
        currentStudent = null;
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        choice1.select(0);  // or default room
        ((DefaultTableModel) jTable1.getModel()).setRowCount(0); // clear table
    }

