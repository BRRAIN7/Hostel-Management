    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        if (currentStudent == null) {
            JOptionPane.showMessageDialog(this, "Search and select a student first.");
            return;
        }

        String name = jTextField2.getText().trim();
        String gender = jTextField3.getText().trim();
        String contact = jTextField4.getText().trim();
        String address = jTextField5.getText().trim();
        String password = jTextField6.getText().trim();
        String selectedRoom = choice1.getSelectedItem();

        StudentController controller = new StudentController();
        boolean success = controller.updateStudent(currentStudent, name, gender, contact, address, password, selectedRoom);

        if (success) {
            JOptionPane.showMessageDialog(this, "Student updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Update failed. Please check input values.");
        }
    }


